#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	chr;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 0 && n <= 9)
	{
		chr = n + '0';
		write(fd, &chr, 1);
	}
	if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		chr = (n % 10) + '0';
		write(fd, &chr, 1);
	}
}
